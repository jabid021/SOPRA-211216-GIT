package exoSpringMvc.model;

public class Adresse {
	public String rue;
	public String ville;
	
	public Adresse() {
		
	}

	public String getRue() {
		return rue;
	}

	public void setRue(String rue) {
		this.rue = rue;
	}

	public String getVille() {
		return ville;
	}

	public void setVille(String ville) {
		this.ville = ville;
	}
	
	
}
